/**
Ethernet：Ethernet是最常见和广泛使用的链路层协议之一。它定义了在局域网（LAN）上进行数据通信的标准，包括数据帧的格式、传输速率、物理接口等。Ethernet协议支持多种传输介质，如以太网（Ethernet）、无线局域网（Wi-Fi）等。

无线局域网协议（Wi-Fi）：Wi-Fi是无线局域网技术的标准，它在链路层上使用一些特定的协议，如IEEE 802.11系列协议。这些协议规定了在无线环境中进行数据传输的方式、频率、编码等。

PPP（Point-to-Point Protocol）：PPP是一种广泛用于点对点连接的链路层协议。它支持串行连接，如拨号连接、DSL连接等。PPP协议提供了认证、错误检测、多路复用等功能，用于可靠地传输数据。

HDLC（High-Level Data Link Control）：HDLC是一种数据链路层协议，用于在广域网（WAN）中进行数据传输。它提供了数据的可靠传输、流量控制和错误检测等功能。HDLC协议常用于传输帧中继（Frame Relay）和同步光纤链路（SONET）等网络中。

MAC（Media Access Control）协议：MAC协议是一组用于控制共享介质访问的协议，如CSMA/CD（Carrier Sense Multiple Access with Collision Detection）和CSMA/CA（Carrier Sense Multiple Access with Collision Avoidance）。这些协议用于协调多个设备共享同一物理介质时的访问冲突。


*/
