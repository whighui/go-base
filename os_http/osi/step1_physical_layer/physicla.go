/**
在OSI七层网络模型中，物理层（Physical Layer）负责在物理媒介上传输原始比特流，处理电气、光学和机械特性，以及提供物理连接和数据传输介质。

以下是一些常见的物理层协议：

Ethernet：Ethernet不仅是链路层协议，也涉及物理层。在物理层上，Ethernet定义了数据的编码和传输方式，如以太网电缆的连接类型、传输速率和信号编码等。常见的以太网物理层标准包括10BASE-T、100BASE-TX、1000BASE-T等。

Wi-Fi：Wi-Fi是一种无线局域网技术，涉及物理层的无线传输。Wi-Fi使用无线电波进行数据传输，其中包括多个物理层标准，如IEEE 802.11a/b/g/n/ac/ax等，它们规定了无线信号的频段、调制方式和传输速率等。

SONET（Synchronous Optical Networking）：SONET是一种光纤通信的物理层协议。它使用光纤作为传输介质，通过光的传输实现高速数据通信。SONET定义了光纤的光学特性、传输速率和接口规范等。

DSL（Digital Subscriber Line）：DSL是一种用于数字用户线路的物理层协议。它利用电话线等铜线作为传输介质，通过调制和解调技术实现高速数据传输。常见的DSL标准包括ADSL（Asymmetric DSL）、VDSL（Very-high-bit-rate DSL）等。

USB（Universal Serial Bus）：USB是一种用于连接计算机和外部设备的物理层协议。它定义了物理接口、电气特性和数据传输方式，支持高速数据传输和供电功能。
*/
